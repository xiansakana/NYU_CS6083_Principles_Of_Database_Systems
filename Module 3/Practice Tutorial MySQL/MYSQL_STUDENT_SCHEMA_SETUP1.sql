from information_schema.tables
where table_name like 'ap%' and table_schema='practice';

-- REM, List of Table Columns 

select tab.table_schema as database_schema,
    tab.table_name as table_name,
    col.ordinal_position as column_id,
    col.column_name as column_name,
    col.data_type as data_type,
    col.CHARACTER_MAXIMUM_LENGTH,
    col.COLUMN_TYPE,
    col.column_key
from information_schema.tables as tab inner join information_schema.columns as col on tab.table_name=col.table_name
where tab.table_name like 'ap%' and tab.table_schema='practice'
order by table_name;

