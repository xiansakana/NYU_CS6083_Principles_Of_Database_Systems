create database practice;

use practice;

CREATE TABLE ap_dept (
    deptno   NUMERIC(2) NOT NULL COMMENT 'DEPARTMENT ID',
    dname    VARCHAR(20) NOT NULL COMMENT 'DEPARTMENT NAME',
    loc      VARCHAR(20) NOT NULL COMMENT 'LOCATION OF THE DEPARTMENT'
);

ALTER TABLE ap_dept  ADD CONSTRAINT ap_dept_pk PRIMARY KEY ( deptno );

CREATE TABLE ap_emp (
    empno      NUMERIC(4) NOT NULL COMMENT 'EMPLOYEE NUMBER',
    efname     VARCHAR(30) NOT NULL COMMENT 'EMPLOYEE FIRST NAME',
    elname     VARCHAR(30) NOT NULL COMMENT 'EMPLOYEE LAST NAME',
    job        VARCHAR(30) NOT NULL COMMENT 'JOB FUNCTION',
    mgr        NUMERIC(4) COMMENT 'EMPLOYEE ID OF THE MANAGER',
    hiredate   DATETIME NOT NULL COMMENT 'HIREDATE OF THE EMPLOYEE',
    sal        DECIMAL(7, 2) NOT NULL COMMENT 'MONTHLY SAL OF EMPLOYEE IN USD',
    comm       DECIMAL (7,2) COMMENT 'COMMISON IN USD',
    deptno     NUMERIC(2) NOT NULL
);

ALTER TABLE ap_emp  ADD CONSTRAINT ap_emp_pk PRIMARY KEY ( empno );

CREATE TABLE ap_proemp (
    hours    NUMERIC(4) NOT NULL,
    empno    NUMERIC(4) NOT NULL,
    projid   NUMERIC(3) NOT NULL);

ALTER TABLE ap_proemp ADD CONSTRAINT ap_proemp_pk PRIMARY KEY ( empno,
                                                                projid );

CREATE TABLE ap_project (
    projid      NUMERIC(3) NOT NULL COMMENT 'PROJECT ID',
    pname       VARCHAR(30) NOT NULL COMMENT 'PROJECT NAME',
    startdate   DATETIME NOT NULL COMMENT 'PROJECT START DATE',
    enddate     DATETIME NOT NULL COMMENT 'PROJECT END DATE'
);


ALTER TABLE ap_project ADD CONSTRAINT ap_project_pk PRIMARY KEY ( projid );


CREATE TABLE ap_salgrade (
    grade   VARCHAR(2) NOT NULL COMMENT 'SAL GRADE',
    hisal   DECIMAL(7, 2) NOT NULL COMMENT 'HIGH SAL RANGE',
    losal   DECIMAL(7, 2) NOT NULL COMMENT 'LOW SAL RANGE'
);

ALTER TABLE ap_salgrade ADD CONSTRAINT ap_salgrade_pk PRIMARY KEY ( grade );

ALTER TABLE ap_emp
    ADD CONSTRAINT ap_emp_ap_dept_fk FOREIGN KEY ( deptno )
        REFERENCES ap_dept ( deptno );

ALTER TABLE ap_proemp
    ADD CONSTRAINT ap_proemp_ap_emp_fk FOREIGN KEY ( empno )
        REFERENCES ap_emp ( empno );

ALTER TABLE ap_proemp
    ADD CONSTRAINT ap_proemp_ap_project_fk FOREIGN KEY ( projid )
        REFERENCES ap_project ( projid );


